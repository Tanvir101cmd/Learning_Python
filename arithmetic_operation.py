a = 8
b = 5

# Addition
print ('Sum: ', a + b)

# Subtration
print ('Subtraction: ', a - b)

# Multiplication
print ('Multiplication: ', a * b)

# Division
print ('Division: ', a / b)

# Floor Division
print ('Floor Division: ', a // b)

# Modulus
print ('Modulus: ', a % b)

# a to the power b
print ('Power:', a ** b)
